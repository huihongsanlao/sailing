package pojo;

public class Weapon {

    private String Code;
    private String name;
    private String type;
    private String weight;
    private String blone;

    public String getCode() {
        return Code;
    }

    public void setCode(String code) {
        Code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getBlone() {
        return blone;
    }

    public void setBlone(String blone) {
        this.blone = blone;
    }
}
